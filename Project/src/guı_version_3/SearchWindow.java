    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        String choose = (String) FilterChoose.getSelectedItem();
        int min = Integer.valueOf(minPrice.getText());
        int max = Integer.valueOf(maxPrice.getText());
        ArrayList<Integer> list = new ArrayList<Integer>();
        if (choose.equals("increasing and decreasing")) {
            list = MainScreen.myOtel.Filter(1, 0, 0);
        } else if (choose.equals("decreasing ascending")) {
            list = MainScreen.myOtel.Filter(2, 0, 0);
        } else if (choose.equals("price range ")) {
            list = MainScreen.myOtel.Filter(3, min, max);
        } else {
            list = MainScreen.myOtel.Filter(4, min, max);
        }
        Object[] room = new Object[list.size()];
        for (int i = 0; i < list.size(); i++) {
            ArrayList<Object> temp = new ArrayList<>();
            temp.add(list.get(i));
            room[i] = temp.toArray();
        }
        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        for (int i = 0; i < room.length; i++) {
            model.addRow((Object[]) room[i]);
        }

    }//GEN-LAST:event_SearchActionPerformed

